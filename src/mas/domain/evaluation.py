"""Evaluation contract: assessment of task execution."""

import uuid
from dataclasses import dataclass, field
from enum import Enum
from statistics import mean
from typing import Any


class EvaluationCriteria(Enum):
    """Standard criteria for evaluating execution quality."""

    CORRECTNESS = "correctness"
    COMPLETENESS = "completeness"
    EFFICIENCY = "efficiency"
    SAFETY = "safety"
    CONSISTENCY = "consistency"


@dataclass
class Evaluation:
    """Assessment of task execution quality.

    Evaluations are generated by the evaluator agent after plan execution.
    They provide structured feedback on whether the execution met requirements.
    """

    id: str
    task_id: str
    plan_id: str
    score: float
    criteria: dict[str, float] = field(default_factory=dict)
    feedback: str = ""
    passed: bool = False
    issues: list[str] = field(default_factory=list)
    metadata: dict[str, Any] = field(default_factory=dict)

    def __post_init__(self) -> None:
        """Validate evaluation on creation."""
        if not self.id:
            raise ValueError("Evaluation id cannot be empty")
        if not self.task_id:
            raise ValueError("Evaluation task_id cannot be empty")
        if not self.plan_id:
            raise ValueError("Evaluation plan_id cannot be empty")
        if not (0.0 <= self.score <= 10.0):
            raise ValueError("Score must be between 0.0 and 10.0")

        # Validate criteria scores
        for criterion, value in self.criteria.items():
            if not (0.0 <= value <= 10.0):
                raise ValueError(f"Criterion {criterion} score must be between 0.0 and 10.0")

    @classmethod
    def from_criteria(
        cls,
        task_id: str,
        plan_id: str,
        criteria_scores: dict[str, float],
        feedback: str = "",
        passed: bool = False,
        issues: list[str] | None = None,
    ) -> "Evaluation":
        """Create evaluation from individual criterion scores.

        Args:
            task_id: ID of the evaluated task
            plan_id: ID of the evaluated plan
            criteria_scores: Dict mapping criteria names to scores (0-10)
            feedback: Human-readable feedback
            passed: Whether execution met requirements
            issues: List of identified issues

        Returns:
            Evaluation with computed overall score
        """
        overall_score = mean(criteria_scores.values()) if criteria_scores else 0.0

        return cls(
            id=str(uuid.uuid4()),
            task_id=task_id,
            plan_id=plan_id,
            score=overall_score,
            criteria=criteria_scores,
            feedback=feedback,
            passed=passed,
            issues=issues or [],
        )

    def meets_minimum_threshold(self, threshold: float = 8.0) -> bool:
        """Check if evaluation meets minimum quality threshold."""
        return self.score >= threshold

    def has_issues(self) -> bool:
        """Check if evaluation identified any issues."""
        return len(self.issues) > 0

    def get_critical_issues(self) -> list[str]:
        """Get issues marked as critical (prefix 'CRITICAL:')."""
        return [issue for issue in self.issues if issue.startswith("CRITICAL:")]
